// Title : 대입연산자
// subTitle : 대입연산자 너어버릴꺼야 ~!!!! 

let value = 1;

// = 대입연산자라고 부름며, value라는 변수에 1을 넣을꺼야!! 
// 즉, 1을 <대입> 할꺼야를 하는 연산자 입니다. 

let a = 1;
a += 1; // a 에게 1을 더하겠다.
console.log(a); // 2

a -= 1; // a 에서 1을 빼겠다.
console.log(a); // 1

a *= 2; // a 에게 2를 곱하겠다.
console.log(a); // 2

a /= 2; // a 를 2 로 나누겠다.
console.log(a); // 1