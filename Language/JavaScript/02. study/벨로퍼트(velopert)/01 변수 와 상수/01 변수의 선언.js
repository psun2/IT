// Title : 변수
// subTitle : 변수의 선언 및 변수란 무엇인가 ?

let value = 1;
// 특정 이름에 특정 값을 담는것 => 선언
// 쉽게 말해서 이제부터 value라는 얘의 값은 1이야
// 변수를 선언할때 let 이라는 키워드를 사용합니다.
console.log(value); // 1

value = 2;
// 변수는 값을 변경할 수 있습니다.

console.log(value); // 2

// let value = 2; // error
// 변수를 선언할때 주의점 : 한번 선언을 했으면 똑같은 이름으로 선언 할수 없습니다.
// SyntaxError: Identifier 'value' has already been declared
// SyntaxError : 식별자 'value'가 이미 선언되었습니다

