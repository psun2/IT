// subTitle : 비교연산자 어떤 숫자가 가장 크니?

const a = 10;
const b = 15;
const c = 15;

// a 가 b 보다 작니 ?
console.log(a < b); // true

// b 가 a 보다 크니 ?
console.log(b > a); // true

// b 가 a 보다 크거나 같니 ?
console.log(b >= a); // true

// a 가 c 보다 작거나 같니 ? 
console.log(a <= c);  // true

// b 가 c 보다 작니 ?
console.log(b < c); // false

// c 가 b 보다 작니 ?
console.log(c < b); // false

// c가 b 보다 작거나 같니?
console.log(c <= b); // true

// c 와 b 가 같니 ?
console.log(c === b); //true